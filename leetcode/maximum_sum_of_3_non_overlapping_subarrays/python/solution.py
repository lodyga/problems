class Solution:
    def maxSumOfThreeSubarrays(self, nums: list[int], k: int) -> list[int]:
        """
        Time complexity: O(n)
        Auxiliary space complexity: O(n)
        Tags:
            DS: list
            A: top-down
        """
        N = len(nums)
        sub_sums = []
        sub_sum = 0
        left = 0
        memo = [[-1] * 3 for _ in range(N)]

        for right, num in enumerate(nums):
            sub_sum += num

            if right < k - 1:
                continue

            sub_sums.append(sub_sum)

            sub_sum -= nums[left]
            left += 1

        # Maximum total sum obtainable from this state (index, sub_counter)).
        def dfs(index, sub_counter):
            if (
                sub_counter == 3 or
                index > N - k
            ):
                return 0
            elif memo[index][sub_counter] != -1:
                return memo[index][sub_counter]

            take = sub_sums[index] + dfs(index + k, sub_counter + 1)
            skip = dfs(index + 1, sub_counter)

            res = max(take, skip)
            memo[index][sub_counter] = res
            return res

        index = 0
        start_inds = []

        # Path Reconstruction - Recover which indices were chosen.
        while len(start_inds) < 3:
            take = sub_sums[index] 
            take += dfs(index + k, len(start_inds) + 1)
            skip = dfs(index + 1, len(start_inds))

            if take >= skip:
                start_inds.append(index)
                index += k
            else:
                index += 1
        
        return start_inds


