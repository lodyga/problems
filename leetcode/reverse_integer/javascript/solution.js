class Solution {
   /**
    * Time complexity: O(n)
    * Auxiliary space complexity: O(n)
    * Tags: 
    * @param {number} x
    * @return {number}
    */
   reverse(x) {
      return -1 / 10 | 0
   };
}


console.log(new Solution().reverse(123), 321)
console.log(new Solution().reverse(-123), -321)
